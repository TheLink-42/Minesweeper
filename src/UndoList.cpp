#include "UndoList.h"


////////////////////////////////////////////////////////////////////////////////
////																		////
////							CONSTRUCTORS								////
////																		////
////////////////////////////////////////////////////////////////////////////////

UndoList::UndoList(): list()
{
	count = 0;
}

UndoList::~UndoList()
{

}


////////////////////////////////////////////////////////////////////////////////
////																		////
////							GETTERS & SETTERS							////
////																		////
////////////////////////////////////////////////////////////////////////////////

PosList	UndoList::get_last() const
{
	return list[count - 1];
}


////////////////////////////////////////////////////////////////////////////////
////																		////
////							MEMBER FUNCTIONS							////
////																		////
////////////////////////////////////////////////////////////////////////////////

void	UndoList::add_last(PosList list)
{
	if (count < MAX_UNDO)
	{
		this->list[count] = list;
		count++;
	}
	else
	{
		for (int i = 0; i < count - 1; i++)
			this->list[i] = this->list[i+1];
		this->list[count - 1] = list;
	}
}

void	UndoList::delete_last()
{
	count--;
}


void	UndoList::destroy()
{
	for (int i = 0; i < count; i++)
		list[i].destroy();
	count = 0;
}
